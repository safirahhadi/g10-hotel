    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        if (request.getParameter("error") != null) {
            request.setAttribute("error", request.getParameter("error"));
        }
        if (request.getParameter("message") != null) {
            request.setAttribute("message", request.getParameter("message"));
        }
        HttpSession session = request.getSession();
        User user = session.getAttribute("user") != null ? (User) session.getAttribute("user") : null;

        if (user == null) {
            request.getRequestDispatcher("user/register.jsp").forward(request, response);
        } else {
            response.sendRedirect(request.getContextPath() + "/listRooms");
            return;
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {

        String firstName = request.getParameter("firstName");
        String lastName = request.getParameter("lastName");
        String contact = request.getParameter("contact");
        String email = request.getParameter("email");
        String password = request.getParameter("password");
        String role = "customer";
        User user = new User(firstName, lastName, contact, email, password, role);
        if (UserDAO.register(user)) {
            response.sendRedirect(request.getContextPath() + "/login?message=Registered successfully");
        } else {
            response.sendRedirect(request.getContextPath() + "/register?error=registration unsuccessful");
        }
    }

